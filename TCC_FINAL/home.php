                        <h2 class="blog-post-title">O que acontecerá no futuro?</h2>
                        
                        <p>O mundo já está passando por diversos problemas ambientais, como por exemplo, auterações climáticas, chuvas de grandes propoprção, há uns anos atrás, algumas pragas
                        de inetos nas plantações, sem contar com os problemas maiores, o mal descarte de lixo, liberação de poluição ao ar, tudo isso resulta no aquecimento global.</p>
                        
                        <p> Para quem não sabe, é o nosso maior poprblema, pensa em um dia quente de verão, você quem ligar o ar condicionando ou o ventilador, porém acaba a energia, 
                        bom o aquecimento global é isto, não vamos ter como fugir do calor. 
                        Tudo isso ocorre devido os gases de efeito estufa liberados na atmosfera, como as industrias fazem, os carros movidos a galosina, etc.
                        </p>

                        <p>Entre esses e outros grandes problemas, o mundo está em constante risco, de derretimento das geleiras, extinsão de animias, tudo isso ocorre porque antigamente não tinhamos o pensamento sustentável.
                        Porém, agora temos, e é a nossa chance de salvar o planeta! 
                        </p>
                        
                        <p>Vai ser um trabalho duro e demorado, mas acrditamos eque é possivel, quem não sonha hoje em dia com um futuro melhor, podendo
