                        <h2 class="blog-post-title">O que é Sustentabilidade?</h2>
                        <p class="blog-post-meta">Junho 10, 2022 por <a href="#" id = "b">Grupo TEC LEAF</a></p>

                        <p>Embora esse seja um termo muito utilizado atualmente, nem todo mundo conhece o seu verdadeiro significado. 
                        Assim, de maneira mais simples, a <strong>sustentabilidade</strong> pode ser entendida como 
                        o conjunto de ações e posturas que deve ser adotado por nós e que visa melhorar a nossa relação com o meio ambiente.</p>
                        
                        <p>Assim, por exemplo, exercemos a sustentabilidade quando nos preocupamos com a forma como os recursos naturais são explorados,
                        quando evitamos o consumo exagerado de produtos que prejudicam o meio ambiente,
                        o desperdício e a degradação da natureza de forma irresponsável.</p>                           
                        
                        <h2>Impactos</h2>

                        <p>Será que sabemos o impacto ambiental que um simples canudo pode gerar para a vida marinha de nosso planeta?
                            Ou melhor… Será que damos a devida importância à vida de outras espécies em nosso planeta? 
                            Pois é, são questionamentos profundos, afinal, não há como dar importância sem uma boa reflexão sobre esse assunto.</p>
                            
                        <p>Outro ponto importante a refletir é sobre a água. Você já parou para pensar na quantidade de água que
                            gastamos diariamente e até quando o planeta terá água potável para suprir toda essa demanda?
                            Controlar o consumo de água, além de ser uma medida sustentável, ajuda também no orçamento familiar.</p>

