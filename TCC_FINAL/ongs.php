                    <h2 class="blog-post-title">O Que São ONGS?</h2>
                    <p class="blog-post-meta">Junho 10, 2022 por <a href="#" id = "b">Grupo TEC LEAF</a></p>

                    <p>Uma Ong é uma entidade sem fins lucrativos, associações e fundações são instituições de natureza jurídica que tem o objetivo de realizar uma mudança social,
                    e que, as arrecadações e receitas são destinadas única e exclusivamente ao patrimônio da própria instituição, no caso, sem a finalidade de acumulação de capital. </p>
                    <p>Em outras palavras isto significa que empregam todo o seu lucro de volta na respectiva entidade.</p>

                    <h2>Função Das ONGS</h2>
                    <p>As ONGs possuem funções importantes na sociedade, pois seus serviços chegam em locais e situações em que o Estado é pouco presente. Muitas vezes as ONGs trabalham em parceria com o Estado.</p>
                    <p>Lá encontrará pessoas unidas por uma causa comum, lutando por ideais que consideram relevantes e, até por isso, focadas e especializadas nesses temas. </p>

                    <h3>ONGS Ambientas e Educação Ambiental</h3>
                    <p>A sua importância é garantir simplismente o futuro e a segurança das gerações futuras. Fornecendo através da educação o apoio e esninamentos iniciais para ajudar ao planeta
                    a se manter seguro e encaminhado para o futuro sustentável. 
                    </p>

                    <h3>Qual a Diferença Entre Instituição, Entidades, Associações E Fundações?</h3> <br>
                    <p> Muitas são as dúvidas quando utilizamos os termos instituição, entidades, associações e fundações,
                    porém o que difere a essas nomenclaturas são apenas particularidades administrativas e jurídicas.</p>

                    <strong><p>Associações</p></strong>
                    <p>Associações são definidas pela formação de grupo de pessoas que se unem por um mesmo objetivo e que, a partir de uma assembleia e estatuto registrado em cartório iniciam suas atividades sem a necessidade de ter um patrimônio e capital.
                    A fiscalização das atividades é realizada pelos próprios associados que devem seguir todas as normativas presente no estatuto.</p>

                    <strong><p>Fundações</p></strong>
                    <p>Fundações se caracterizam por serem criadas a partir de uma pessoa ou instituidor que possua um patrimônio suficiente para iniciar as atividades propostas. Todas as atividades como o funcionamento, 
                    aprovações e alterações da política e estatuto são regidas pelo ministério público e a vontade do instituidor prevalece em todos os momentos.</p>
                
                    <h2>Instituições, Organizações e Entidades</h2>
                    <p>Os termos instituição, organização e entidade sem fins lucrativos são termos genéricos para se referir as diferentes pessoas jurídicas sem fins econômicos, sendo utilizados frequentemente essas termologias para associações ou fundações. </p>
                    <p>Portanto, são apenas sinônimos mais amplos para as duas formas jurídicas explicadas anteriormente.</p>
